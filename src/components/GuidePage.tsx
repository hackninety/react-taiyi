export function GuidePage() {
  const [active, setActive] = useState<string>(SECTIONS[0].id);

  const jump = (id: string) => {
    setActive(id);
    document.getElementById(`g-${id}`)?.scrollIntoView({ behavior: 'smooth', block: 'start' });
  };

  return (
    <div className="guide">
      <aside className="guide-toc">
        <div className="guide-toc-head">目 录</div>
        {SECTIONS.map((s) => (
          <button
            key={s.id}
            type="button"
            className={`toc-item ${active === s.id ? 'active' : ''}`}
            onClick={() => jump(s.id)}
          >
            {s.title}
          </button>
        ))}
      </aside>
      <div className="guide-body">
        {SECTIONS.map((s) => (
          <section key={s.id} id={`g-${s.id}`} className="card guide-section">
            <h3>{s.title}</h3>
            {s.body}
          </section>
        ))}
      </div>
    </div>
  );
}
