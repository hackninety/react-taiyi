/** 所占何事聚焦段（未设置时为空串） */
function inquiryBlock(payload: ExportPayload): string {
  const q = payload.inquiry;
  if (!q) return '';
  const emphasis = TOPIC_EMPHASIS[q.topic] ?? '';
  return `\n**本次所占（分析聚焦）**：【${q.topic}】${q.text ?? ''}——请围绕此事组织全部分析与断语；${emphasis}与所问无关的部分简述即可。\n`;
}

